public class Tower
{
    private int width;
    private int xPos;
    private int height;


    public Tower(int xPos, int height, int width)
    {
        this.xPos = xPos;
        this.height = height;
        this.width = width;
    }


    public int getxPos()
    {
        return xPos;
    }


    public int getHeight()
    {
        return height;
    }

    
    public int getWidth()
    {
        return width;
    }

}
